export function anonymize(text) {
  let t = text.replace(/\b\d{4}[\s-]?\d{4}[\s-]?\d{4}[\s-]?\d{4}\b/g, "****-****-****-****");
  t = t.replace(/\b\d{12}\b/g, "****-****-****");
  return t;
}

export function analyzeTextForFraud(text) {
  const lower = text.toLowerCase();
  const kzHits = FRAUD_KEYWORDS_KZ.filter(k => lower.includes(k));
  const ruHits = FRAUD_KEYWORDS_RU.filter(k => lower.includes(k));
  const allHits = [...new Set([...kzHits, ...ruHits])];
  const score = Math.min(allHits.length * 22, 100);
  return { score, hits: allHits, safe: score < 30 };
}
